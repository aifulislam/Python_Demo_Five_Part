#Python Type casting----

#int()
print("Integer----")
x = int(1)
y = int(2.8)
z = int("3")
print(x)
print(y)
print(z)

#float()
print("Float----")
x = float(1)
y = float(2.8)
z = float("3")
print(x)
print(y)
print(z)

#str()
print("String----")
x = str("s1")
y = str(2.8)
z = str(2)
print(x)
print(y)
print(z)
