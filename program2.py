#Python Data type---------

#str
x = "Arif"
print(x)
print(type(x))

#int
x = 5
print(x)
print(type(x))

#float
x = 10.5
print(x)
print(type(x))

#complex
x = 5j
print(x)
print(type(x))

#list
x = ["Arif", "Tamim", "Nazim","Nasim"]
print(x)
print(type(x))

#Tuple
x = ("Arif", "Tamim", "Nazim","Nasim")
print(x)
print(type(x))

#range
x = range(6)
print(x)
print(type(x))

#dict
x = {"Arif" : "Tamim" , "Nazim" : "Nasim"}
print(x)
print(type(x))

#set
x = {"Arif" , "Tamim" , "Nazim" , "Nasim"}
print(x)
print(type(x))

#frozenset
x = frozenset({"Arif" , "Tamim" , "Nazim" , "Nasim"})
print(x)
print(type(x))

#bool
x = True
print(x)
print(type(x))

#bytes
x = b"Arif"
print(x)
print(type(x))

#bytearray
x = bytearray(5)
print(x)
print(type(x))

#memoryview
x = memoryview(bytes(5))
print(x)
print(type(x))

